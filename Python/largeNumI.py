def last_digit(n1, n2):
  if n2 == 0:
      return 1
  if n1 == 0:
      return 0
  powers = [[0,0,0,0],   
            [1,1,1,1],  
            [6,2,4,8],  
            [1,3,9,7],  
            [6,4,6,4],  
            [5,5,5,5],  
            [6,6,6,6],  
            [1,7,9,3],  
            [6,8,4,2],  
            [1,9,1,9]] 
  
  return powers[n1%10][n2%4]
